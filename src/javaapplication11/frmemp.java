    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        if(txtnam.getText().equals("") || txtphn.getText().equals(""))
      JOptionPane.showMessageDialog(this,"Please fill entries");
      else  
      {
            try {
                empprp objprp=new empprp();
                clsemp obj=new clsemp();
                objprp.setEmpnam(txtnam.getText());
                objprp.setEmpphnno(txtphn.getText());
                objprp.setEmploc(cmbloc.getSelectedItem().toString());
                String s=txtpic.getText();
                if(s.equals("")==true)
                {
                    try {
                        empprp k =obj.findemp(ecod);
                        objprp.setEmppic(k.getEmppic());
                    } catch (IOException ex) {
                        Logger.getLogger(frmemp.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(frmemp.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (SQLException ex) {
                        Logger.getLogger(frmemp.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else
                {
                    s=s.substring(s.lastIndexOf("\\")+1);
                    objprp.setEmppic(s);
                }
                objprp.setEmpcod(ecod);
                
                obj.updemp(objprp);
                deletebtn.setEnabled(false);
                updatebtn.setEnabled(false);
                 //code to clear boxes and fill table
                saveimage();
                clear();
                loaddata();
                
            } catch (IOException ex) {
                Logger.getLogger(frmemp.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmemp.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(frmemp.class.getName()).log(Level.SEVERE, null, ex);
            }
      }
    }//GEN-LAST:event_updatebtnActionPerformed

