    public void saveimage()
    {
         try {
             Path source=Paths.get(txtpic.getText());
             Path targetDir=Paths.get("D:\\java\\complaint management\\src\\emppics");
             Files.createDirectories(targetDir);//if dir doesnt exist , create new
             String s=txtpic.getText();
             s=s.substring(s.lastIndexOf("\\")+1);
             Path Target=targetDir.resolve(s);
             Files.copy(source, Target,StandardCopyOption.REPLACE_EXISTING);
         } catch (IOException ex) {
             Logger.getLogger(frmemp.class.getName()).log(Level.SEVERE, null, ex);
         }
    }
    
    private void loaddata() throws IOException,ClassNotFoundException,SQLException,NullPointerException
    {
        DefaultTableModel mod=(DefaultTableModel)(jTable1.getModel());
        clsemp obj=new clsemp();
        List<empprp> k=obj.dspemp();
        mod.setRowCount(0);
        //JOptionPane.showMessageDialog(this,"null entry");
        for(empprp p:k)
        {
            Object[] rowdata=new Object[5];
            rowdata[0]=p.getEmpcod();
            rowdata[1]=p.getEmpnam();
            rowdata[2]=p.getEmpphnno();
            rowdata[3]=p.getEmploc();
            ImageIcon img=new ImageIcon(getClass().getResource("/emppics/"+p.getEmppic()));
            rowdata[4]=img;
            mod.addRow(rowdata);
        }
        
        // JOptionPane.showMessageDialog(this,"bherfk");
        jTable1.setModel(mod);              
    }

